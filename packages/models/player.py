class Player:
    """Player."""

    def __init__(self, last_name, first_name, birth_date, sex, rank):
        """Has a last name, a first name, a date of birth, a sex and a rank."""
        self.last_name = last_name
        self.first_name = first_name
        self.birth_date = birth_date
        self.sex = sex
        self.rank = rank

